package com.aresoft.manager.model;

public class OdsDisBank {
  private String id;
  private String stru_id;
  private String stru_fname;
  private String stru_sname;
  private String stru_detail;
  private String memo;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getStru_id() {
    return stru_id;
  }

  public void setStru_id(String stru_id) {
    this.stru_id = stru_id;
  }

  public String getStru_fname() {
    return stru_fname;
  }

  public void setStru_fname(String stru_fname) {
    this.stru_fname = stru_fname;
  }

  public String getStru_sname() {
    return stru_sname;
  }

  public void setStru_sname(String stru_sname) {
    this.stru_sname = stru_sname;
  }

  public String getStru_detail() {
    return stru_detail;
  }

  public void setStru_detail(String stru_detail) {
    this.stru_detail = stru_detail;
  }

  public String getMemo() {
    return memo;
  }

  public void setMemo(String memo) {
    this.memo = memo;
  }
}
